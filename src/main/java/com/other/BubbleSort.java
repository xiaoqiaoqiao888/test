package com.other;

/**
 * 冒泡排序算法实现类
 * 该类演示了使用冒泡排序算法对整数数组进行排序的过程
 */
public class BubbleSort {
    /**
     * 程序主入口方法
     * 该方法实现了冒泡排序算法，对一个整型数组进行排序并输出结果
     *
     * @param args 命令行参数，本程序中未使用
     */
    public static void main(String[] args) {
        // 初始化一个待排序的整型数组
        // 数组元素为：{1, 5, 4, 8, 16, 9, 6, 3, 0}
        int[] arr = {1, 5, 4, 8, 16, 9, 6, 64, 3, 0};

        // 外层循环控制排序轮数
        // 对于长度为n的数组，需要进行n-1轮排序
        for (int i = 0; i < arr.length - 1; i++) {
            // 内层循环控制每轮比较次数
            // 每轮排序后，最大的元素会"冒泡"到数组末尾，
            // 因此每轮比较的次数比上一轮少一次
            for (int j = 0; j < arr.length - i - 1; j++) {
                // 比较相邻元素，如果前一个元素大于后一个元素，则交换它们的位置
                // 这是冒泡排序的核心比较逻辑，目的是将较大的元素向后移动
                //从小到大排序，如果是从大到小if改成<
                if (arr[j] > arr[j + 1]) {
                    // 使用临时变量完成两个元素值的交换
                    // 交换过程：先将前一个元素的值存入临时变量，
                    //然后将后一个元素的值赋给前一个元素，
                    //最后将临时变量的值赋给后一个元素
                    int tmp = arr[j];
                    arr[j] = arr[j + 1];
                    arr[j + 1] = tmp;
                }
            }
        }
        // 遍历并输出排序后的数组元素
        // 此时数组已经按照从小到大的顺序排列
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}
